package com.company;

public class Car {
    final String producer;
    final String model;
    int year;
    String color;
    Double value;

    public Car(String producer, String model, int year, String color, Double value){
        this.producer = producer;
        this.model = model;
        this.year = year;
        this.color = color;
        this.value = value;
    }





}

